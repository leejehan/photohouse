package com.willbe.dto;

public class ScheduleDTO {
	String schedule_id;
	String schedule_year;
	String schedule_Month;
	String schedule_Day;
	String schedule_time;
	String schedule_title;
	String schedule_content;
	
	public String getSchedule_id() {
		return schedule_id;
	}
	public void setSchedule_id(String schedule_id) {
		this.schedule_id = schedule_id;
	}
	public String getSchedule_year() {
		return schedule_year;
	}
	public void setSchedule_year(String schedule_year) {
		this.schedule_year = schedule_year;
	}
	public String getSchedule_Month() {
		return schedule_Month;
	}
	public void setSchedule_Month(String schedule_Month) {
		this.schedule_Month = schedule_Month;
	}
	public String getSchedule_Day() {
		return schedule_Day;
	}
	public void setSchedule_Day(String schedule_Day) {
		this.schedule_Day = schedule_Day;
	}
	public String getSchedule_time() {
		return schedule_time;
	}
	public void setSchedule_time(String schedule_time) {
		this.schedule_time = schedule_time;
	}
	public String getSchedule_title() {
		return schedule_title;
	}
	public void setSchedule_title(String schedule_title) {
		this.schedule_title = schedule_title;
	}
	public String getSchedule_content() {
		return schedule_content;
	}
	public void setSchedule_content(String schedule_content) {
		this.schedule_content = schedule_content;
	}
	
	
}
